package org.example.toolz;

import org.tomitribe.crest.api.Command;
import org.tomitribe.crest.api.Default;
import org.tomitribe.crest.api.Option;
import org.tomitribe.crest.api.StreamingOutput;
import org.tomitribe.crest.val.Readable;
import org.tomitribe.util.IO;

import java.io.File;
import java.io.IOException;
import java.io.OutputStream;

/**
 *
 */
public class App {

    @Command
    public StreamingOutput cat(@IsFile @Readable final File file) {

        return new StreamingOutput() {
            @Override
            public void write(final OutputStream os) throws IOException {
                IO.copy(file, os);
            }
        };
    }

    @Command
    public String hello(@Option("name") @Default("World") final String name,
                        @Option("language") @Default("EN") final Language language) {
        return String.format("%s, %s!", language.getSalutation(), name);
    }

    public static enum Language {
        EN("Hello"),
        ES("Hola"),
        FR("Bonjour");

        private final String salutation;

        private Language(final String salutation) {
            this.salutation = salutation;
        }

        public String getSalutation() {
            return salutation;
        }
    }

}
